#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*cp;
	int		i;

	i = 0;
	while (src[i])
		i++;
	cp = (char *) malloc(sizeof(char) * i);
	i = 0;
	while (src[i])
	{
		cp[i] = src[i];
		i++;
	}
	cp[i] = '\0';
	return (cp);
}
