int	ft_sqrt(int nb)
{
	int	i;

	if (nb <= 0)
		return (0);
	i = 0;
	while (i * i <= nb && i <= 46341)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
